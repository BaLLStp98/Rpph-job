export async function GET(request: NextRequest) {
  try {
    const { searchParams } = new URL(request.url);
    const resumeDepositId = searchParams.get('resumeDepositId');

    console.log('üîç API Resume Documents - resumeDepositId:', resumeDepositId);

    if (!resumeDepositId) {
      return NextResponse.json(
        { success: false, message: '‡∏Å‡∏£‡∏∏‡∏ì‡∏≤‡∏£‡∏∞‡∏ö‡∏∏ resumeDepositId' },
        { status: 400 }
      );
    }

    // ‡∏î‡∏∂‡∏á‡∏£‡∏≤‡∏¢‡∏Å‡∏≤‡∏£‡πÄ‡∏≠‡∏Å‡∏™‡∏≤‡∏£
    const documents = await prisma.resumeDepositDocument.findMany({
      where: { resumeDepositId },
      orderBy: { createdAt: 'desc' }
    });

    console.log('üìÑ Found documents:', documents.length);

    const formattedDocuments = documents.map(doc => ({
      id: doc.id,
      resumeDepositId: doc.resumeDepositId,
      documentType: doc.documentType,
      fileName: doc.fileName,
      filePath: doc.filePath,
      fileSize: doc.fileSize,
      mimeType: doc.mimeType,
      createdAt: doc.createdAt,
    }));

    console.log('‚úÖ Returning documents:', formattedDocuments);

    return NextResponse.json({
      success: true,
      data: formattedDocuments
    });

  } catch (error) {
    console.error('Error fetching resume documents:', error);
    return NextResponse.json(
      { success: false, message: '‡πÄ‡∏Å‡∏¥‡∏î‡∏Ç‡πâ‡∏≠‡∏ú‡∏¥‡∏î‡∏û‡∏•‡∏≤‡∏î‡πÉ‡∏ô‡∏Å‡∏≤‡∏£‡∏î‡∏∂‡∏á‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡πÄ‡∏≠‡∏Å‡∏™‡∏≤‡∏£' },
      { status: 500 }
    );
  }
}

export async function POST(request: NextRequest) {
  try {
    const formData = await request.formData();
    const file = formData.get('file') as File;
    const resumeDepositId = formData.get('resumeDepositId') as string;
    const documentType = formData.get('documentType') as string;

    if (!file || !resumeDepositId || !documentType) {
      return NextResponse.json(
        { success: false, message: '‡∏Å‡∏£‡∏∏‡∏ì‡∏≤‡∏£‡∏∞‡∏ö‡∏∏‡πÑ‡∏ü‡∏•‡πå, resumeDepositId ‡πÅ‡∏•‡∏∞ documentType' },
        { status: 400 }
      );
    }

    // ‡∏ï‡∏£‡∏ß‡∏à‡∏™‡∏≠‡∏ö‡∏Ç‡∏ô‡∏≤‡∏î‡πÑ‡∏ü‡∏•‡πå (10MB)
    if (file.size > 10 * 1024 * 1024) {
      return NextResponse.json(
        { success: false, message: '‡∏Ç‡∏ô‡∏≤‡∏î‡πÑ‡∏ü‡∏•‡πå‡∏ï‡πâ‡∏≠‡∏á‡πÑ‡∏°‡πà‡πÄ‡∏Å‡∏¥‡∏ô 10MB' },
        { status: 400 }
      );
    }

    // ‡∏ï‡∏£‡∏ß‡∏à‡∏™‡∏≠‡∏ö‡∏õ‡∏£‡∏∞‡πÄ‡∏†‡∏ó‡πÑ‡∏ü‡∏•‡πå
    const allowedTypes = ['application/pdf', 'image/jpeg', 'image/jpg', 'image/png'];
    if (!allowedTypes.includes(file.type)) {
      return NextResponse.json(
        { success: false, message: '‡∏£‡∏≠‡∏á‡∏£‡∏±‡∏ö‡πÄ‡∏â‡∏û‡∏≤‡∏∞‡πÑ‡∏ü‡∏•‡πå PDF, JPG, JPEG, PNG ‡πÄ‡∏ó‡πà‡∏≤‡∏ô‡∏±‡πâ‡∏ô' },
        { status: 400 }
      );
    }

    // ‡∏™‡∏£‡πâ‡∏≤‡∏á‡∏ä‡∏∑‡πà‡∏≠‡πÑ‡∏ü‡∏•‡πå‡πÉ‡∏´‡∏°‡πà
    const timestamp = Date.now();
    const fileExtension = file.name.split('.').pop();
    const fileName = `${resumeDepositId}-${documentType}-${timestamp}.${fileExtension}`;
    
    // ‡∏™‡∏£‡πâ‡∏≤‡∏á‡πÇ‡∏ü‡∏•‡πÄ‡∏î‡∏≠‡∏£‡πå‡∏ñ‡πâ‡∏≤‡∏¢‡∏±‡∏á‡πÑ‡∏°‡πà‡∏°‡∏µ
    const uploadDir = join(process.cwd(), 'public', 'uploads', 'resume-documents');
    await mkdir(uploadDir, { recursive: true });

    // ‡∏ö‡∏±‡∏ô‡∏ó‡∏∂‡∏Å‡πÑ‡∏ü‡∏•‡πå
    const filePath = join(uploadDir, fileName);
    const bytes = await file.arrayBuffer();
    const buffer = Buffer.from(bytes);
    await writeFile(filePath, buffer);

    // ‡∏ö‡∏±‡∏ô‡∏ó‡∏∂‡∏Å‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡πÉ‡∏ô‡∏ê‡∏≤‡∏ô‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•
    const document = await prisma.resumeDepositDocument.create({
      data: {
        resumeDepositId,
        documentType,
        fileName: file.name,
        filePath: `/uploads/resume-documents/${fileName}`,
        fileSize: file.size,
        mimeType: file.type,
      }
    });

    return NextResponse.json({
      success: true,
      message: '‡∏≠‡∏±‡∏õ‡πÇ‡∏´‡∏•‡∏î‡πÑ‡∏ü‡∏•‡πå‡πÄ‡∏£‡∏µ‡∏¢‡∏ö‡∏£‡πâ‡∏≠‡∏¢‡πÅ‡∏•‡πâ‡∏ß',
      data: {
        id: document.id,
        fileName: document.fileName,
        filePath: document.filePath,
        fileSize: document.fileSize,
        mimeType: document.mimeType,
      }
    });

  } catch (error) {
    console.error('Error uploading resume document:', error);
    return NextResponse.json(
      { success: false, message: '‡πÄ‡∏Å‡∏¥‡∏î‡∏Ç‡πâ‡∏≠‡∏ú‡∏¥‡∏î‡∏û‡∏•‡∏≤‡∏î‡πÉ‡∏ô‡∏Å‡∏≤‡∏£‡∏≠‡∏±‡∏õ‡πÇ‡∏´‡∏•‡∏î‡πÑ‡∏ü‡∏•‡πå' },
      { status: 500 }
    );
  }
}

export async function DELETE(request: NextRequest) {
  try {
    const body = await request.json();
    const { resumeDepositId, documentType } = body;

    if (!resumeDepositId || !documentType) {
      return NextResponse.json(
        { success: false, message: '‡∏Å‡∏£‡∏∏‡∏ì‡∏≤‡∏£‡∏∞‡∏ö‡∏∏ resumeDepositId ‡πÅ‡∏•‡∏∞ documentType' },
        { status: 400 }
      );
    }

    // ‡∏•‡∏ö‡πÄ‡∏≠‡∏Å‡∏™‡∏≤‡∏£‡∏ï‡∏≤‡∏° documentType
    const deletedDocument = await prisma.resumeDepositDocument.deleteMany({
      where: {
        resumeDepositId: resumeDepositId,
        documentType: documentType
      }
    });

    return NextResponse.json({
      success: true,
      message: '‡∏•‡∏ö‡πÄ‡∏≠‡∏Å‡∏™‡∏≤‡∏£‡πÄ‡∏£‡∏µ‡∏¢‡∏ö‡∏£‡πâ‡∏≠‡∏¢‡πÅ‡∏•‡πâ‡∏ß',
      deletedCount: deletedDocument.count
    });

  } catch (error) {
    console.error('Error deleting resume document:', error);
    return NextResponse.json(
      { success: false, message: '‡πÄ‡∏Å‡∏¥‡∏î‡∏Ç‡πâ‡∏≠‡∏ú‡∏¥‡∏î‡∏û‡∏•‡∏≤‡∏î‡πÉ‡∏ô‡∏Å‡∏≤‡∏£‡∏•‡∏ö‡πÄ‡∏≠‡∏Å‡∏™‡∏≤‡∏£' },
      { status: 500 }
    );
  }
}